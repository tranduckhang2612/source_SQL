CREATE TABLE PHONGBAN (
    TenPhong VARCHAR(15) NOT NULL,
    MaPhong INT NOT NULL,
    SoCCCD_TruongPhong CHAR(9) NULL, 
    NgayNhanChuc SMALLDATETIME,
    SoLuongNhanVien INT,
    CONSTRAINT PK_PHONGBAN PRIMARY KEY (MaPhong)
);

CREATE TABLE NHANVIEN (
    TenLot VARCHAR(15) NOT NULL,
    ChuCaiDauGiua CHAR(1),
    Ten VARCHAR(15) NOT NULL,
    SoCCCD CHAR(9) NOT NULL,
    NgaySinh SMALLDATETIME NULL,
    DiaChi VARCHAR(30),
    GioiTinh CHAR(1),
    Luong DECIMAL(18,2),
    SoCCCD_NguoiQL CHAR(9),
    MaPhong INT NOT NULL,
    NgayVaoLam SMALLDATETIME NULL,
    CONSTRAINT PK_NHANVIEN PRIMARY KEY (SoCCCD),
    CONSTRAINT FK_NHANVIEN_NHANVIEN FOREIGN KEY (SoCCCD_NguoiQL) REFERENCES NHANVIEN (SoCCCD),
    CONSTRAINT FK_NHANVIEN_PHONGBAN FOREIGN KEY (MaPhong) REFERENCES PHONGBAN (MaPhong)
);


